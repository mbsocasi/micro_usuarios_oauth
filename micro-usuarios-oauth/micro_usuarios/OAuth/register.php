<?php
require_once 'config.php';

$error = '';
$success = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $usuario = [
        'nombre' => $_POST['nombre'],
        'apellido' => $_POST['apellido'],
        'email' => $_POST['email'],
        'password' => hashPassword($_POST['password']),
        'telefono' => $_POST['telefono'],
        'fechaNacimiento' => $_POST['fechaNacimiento']
    ];

    $result = callAPI('POST', API_AUTH_URL . '/register', $usuario);

    if (isset($result['message']) && $result['message'] === 'Usuario registrado exitosamente') {
        $success = 'Registro exitoso. Por favor, inicia sesión.';
    } else {
        $error = $result['message'] ?? 'Error al registrar el usuario';
    }
}
